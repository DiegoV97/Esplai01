let v = 5 //valor inicial de v
let x = 0 // valor inicial de x
while (v > 0) { // Condicion = mientras que el valor de v sea mayor a 0; pasa lo que esta { entre llaves }
    x = x + v; // a X se le asigna el valor actual de x + el valor actual de v
    v-- // el valor de v se reduce en 1
}
console.log(x)

